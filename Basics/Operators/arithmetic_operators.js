let x = 2;
let y = 8;

console.log("Addition Operator: x + y = " + (x+y));
console.log("Subtraction Operator: x - y = " + (x-y));
console.log("Multiplication Operator: x * y = " + (x*y));
console.log("Division Operator: x / y = " + (x/y));
console.log("Modulus Operator: x % y = " + (x%y));
console.log("Exponentiation Operator: x ** y = " + (x**y));

// Increment Operator
console.log("Pre-increment Operator: ++x = " + (++x));
console.log("Post-increment Operator: x++" + (x++));

// Decrement Operator
console.log("Pre-decrement Operator: --x = " + (--x));
console.log("Post-decrement Operator: x--" + (x--));
