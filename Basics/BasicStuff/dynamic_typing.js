let name = "Radhey";
console.log(typeof name);

// There is no int or float in JavaScript, its just a number
name = 18;
console.log(typeof name);

// It is also a number
name = 18.5;
console.log(typeof name);

name = true;
console.log(typeof name);

name = undefined;
console.log(typeof name);

// Null is actually an object!
name = null;
console.log(typeof name);
