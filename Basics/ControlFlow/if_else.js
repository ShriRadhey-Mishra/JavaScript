let hour = 10;

if (hour >= 0 && hour < 12) {
    console.log("Good Morning, Anjalii!!");
}

else if (hour >= 12 && hour < 17) {
    console.log("Good Afternoon, Anjalii!!");
}

else if (hour >= 17 && hour < 20) {
    console.log("Good Evening, Anjalii!!");
}

else {
    console.log("Good Night, Anjalii!!");
}